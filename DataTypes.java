public class DataTypes {
    public static void main(String args[])
    {
       byte b = 12;
       short sh = 123;
       int i = 1234567890;
       float f = 234.65f;
       boolean bool = true;

        System.out.println("Data type byte: " + b );
        System.out.println("Data type short: " + sh );
        System.out.println("Data type int: " + i );
        System.out.println("Data type float: " + f );
        System.out.println("Data type boolean: " + bool );

    }
}